package Chapter1;
public class Literals {
 public static void main(String[] args) {
    int a = 50; // Integer literal 
    double pie = 3.14; //double literal
    char ch = 'A'; //character literals 
    float f=3.14f; //float literals 
    boolean isJavaFun= true ; //boolean literlas
    String strt= "ANUJ"; //String literal

    System.out.println("Integer Literal:"+a);
    System.out.println("Double Literal:"+pie);
    System.out.println("Character Literal:"+ch);
    System.out.println("Float Literal:"+f);
    System.out.println("Boolean Literal:"+isJavaFun);
    System.out.println("String Literal:"+strt);
 }   
}